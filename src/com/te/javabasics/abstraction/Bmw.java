package com.te.javabasics.abstraction;

public class Bmw implements Car {

	public void accelerator()
	{
		System.out.println("BMW is Accelerating");
	}
	public void brake()
	{
		System.out.println("BMW is Stopped");
	}
	public void gear()
	{
		System.out.println("BMW's gear Mode ON");
	}
	public void gps()
	{
		System.out.println("GPS is Available");
	}
	public void carPlay() {
		System.out.println("BMW is Having Car Play");
		
	}
	
	
}
